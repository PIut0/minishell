#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*r;
	char	*t;

	t = (char *)s;
	r = 0;
	while (*t)
	{
		if (*t == c)
			r = t;
		t++;
	}
	if (*t == c)
		r = t;
	return (r);
}

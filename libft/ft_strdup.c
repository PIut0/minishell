#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	n;
	size_t	i;
	char	*t;

	n = ft_strlen(s1);
	if (!(t = (char*)malloc(n + 1)))
		return (0);
	i = -1;
	while (++i < n)
		t[i] = s1[i];
	t[i] = 0;
	return (t);
}
